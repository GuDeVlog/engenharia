using Microsoft.AspNetCore.Http;
using System;

namespace CadAlunoMVC.Controllers
{
    public static class HelperControllers
    {
        public static Boolean VerificaUserLogado(ISession session)
        {
            string logado = session.GetString("Logado");
            if (logado == null)
                return false;
            else
                return true;
        }
    }
}
